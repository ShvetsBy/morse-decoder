const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

let arr = [],   
    letters = [],
    point = /10/g,
    dash = /11/g,
    zero = /0/g,
    space = /\*{10}/g;
function decode(expr) {
    for (i = 0; i < expr.length; i = i + 10) {
        letters.push(expr.slice(i, i+ 10));
    }
    const morseSymbols = letters.map((arg) => arg.replace (point, '.').replace(dash, '-').replace (zero, '').replace(space, ' ')); //каждый элемент массива, передается в функцию, как аргумент. К нему применяются действия по замене элементов.
//    console.log(morseSymbols);
    // [".-.", "---", ".-", "-..", " ", "-", "---", " ", ".--.", ".-.", "---", "--.", ".-.", ".-", "--", "--", "..", "-.", "--."]
    morseSymbols.forEach((arg) => arr.push(MORSE_TABLE[arg] || ' '));
    // console.log(arr.join(''));
    return arr.join('');
}
module.exports = {
    decode
}

